# Sanitisation des cha√Ænes pour injection dans fichiers de configuration
sanitize_string() {
    local input="$1"
    local max_length="${2:-100}"
    
    # Suppression des caract√®res dangereux et limitation de longueur
    echo "$input" | sed 's/[^a-zA-Z0-9._-]//g' | cut -c1-"$max_length"
}

# Sanitisation des ports
sanitize_port() {
    local port="$1"
    
    # Ne garder que les chiffres
    port=$(echo "$port" | sed 's/[^0-9]//g')
    
    # V√©rifier la plage valide
    if [[ -n "$port" ]] && [[ "$port" -ge 1 ]] && [[ "$port" -le 65535 ]]; then
        echo "$port"
    else
        return 1
    fi
}

# Sanitisation des chemins de fichiers
sanitize_path() {
    local path="$1"
    
    # R√©solution du chemin absolu et v√©rification
    if [[ "$path" =~ ^/[a-zA-Z0-9/_.-]+$ ]]; then
        realpath -m "$path" 2>/dev/null || echo "$path"
    else
        return 1
    fi
}

# Validation s√©curis√©e des entr√©es utilisateur
validate_user_input() {
    local input="$1"
    local type="$2"
    
    case "$type" in
        "service_name")
            [[ "$input" =~ ^[a-zA-Z0-9_-]+$ ]] && [[ ${#input} -le 50 ]]
            ;;
        "port")
            [[ "$input" =~ ^[0-9]+$ ]] && [[ "$input" -ge 1 ]] && [[ "$input" -le 65535 ]]
            ;;
        "bantime")
            [[ "$input" == "-1" ]] || [[ "$input" =~ ^[0-9]+[smhdwy]?$ ]]
            ;;
        "logpath")
            [[ "$input" =~ ^/[a-zA-Z0-9/_.-]+$ ]] && [[ ${#input} -le 255 ]]
            ;;
        *)
            return 1
            ;;
    esac
}

