# Validation compl√®te d'un port avec contexte
validate_port_advanced() {
    local port="$1"
    local service="${2:-unknown}"
    
    # Sanitisation
    if ! port=$(sanitize_port "$port"); then
        warning "Port invalide pour $service : '$1'"
        return 1
    fi
    
    # V√©rification si le port est d√©j√† utilis√©
    if netstat -tuln 2>/dev/null | grep -q ":${port} "; then
        info "Port $port d√©j√† en √©coute (normal pour $service)"
    fi
    
    # Ports privil√©gi√©s
    if [[ $port -lt 1024 ]] && [[ $EUID -ne 0 ]]; then
        warning "Port privil√©gi√© $port n√©cessite les droits root"
    fi
    
    echo "$port"
    return 0
}

# Validation avanc√©e du temps de bannissement
validate_bantime_advanced() {
    local bantime="$1"
    
    if [[ "$bantime" == "-1" ]] || [[ "$bantime" == "permanent" ]]; then
        echo "-1"
        return 0
    fi
    
    if [[ "$bantime" =~ ^[0-9]+$ ]]; then
        echo "$bantime"
        return 0
    fi
    
    if [[ "$bantime" =~ ^([0-9]+)([smhdwy])$ ]]; then
        local number="${BASH_REMATCH[1]}"
        local unit="${BASH_REMATCH[2]}"
        
        case "$unit" in
            s) echo "$number" ;;
            m) echo "$((number * 60))" ;;
            h) echo "$((number * 3600))" ;;
            d) echo "$((number * 86400))" ;;
            w) echo "$((number * 604800))" ;;
            y) echo "$((number * 31536000))" ;;
        esac
        return 0
    fi
    
    return 1
}

# V√©rification approfondie des fichiers de log
check_logfile_advanced() {
