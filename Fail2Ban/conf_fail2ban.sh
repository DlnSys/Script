# Sauvegarde s√©curis√©e de la configuration existante
backup_existing_config() {
    info "Sauvegarde de la configuration existante..."
    
    if [[ "$SIMULATE_MODE" == "true" ]]; then
        simulate "Sauvegarde dans : $BACKUP_DIR"
        return 0
    fi
    
    if [[ -d /etc/fail2ban ]]; then
        # Copie avec pr√©servation des attributs
        if cp -rp /etc/fail2ban/* "$BACKUP_DIR/" 2>/dev/null; then
            success "Configuration sauvegard√©e dans $BACKUP_DIR"
            
            # Cr√©ation d'un manifest de sauvegarde
