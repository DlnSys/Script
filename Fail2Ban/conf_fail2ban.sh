# Mise √† jour et installation des paquets avec gestion d'erreur
install_packages() {
    info "V√©rification et installation des paquets requis..."
    
    if [[ "$SIMULATE_MODE" == "true" ]]; then
        simulate "Installation des paquets : ${REQUIRED_PACKAGES[*]}"
        return 0
    fi
    
    # Test de connectivit√© r√©seau
    if ! ping -c 1 -W 5 8.8.8.8 &>/dev/null; then
        warning "Connectivit√© r√©seau limit√©e, mise √† jour des paquets ignor√©e"
    else
        info "Mise √† jour de la liste des paquets..."
        if ! apt update; then
            error_exit "Impossible de mettre √† jour la liste des paquets"
        fi
    fi
    
    # Installation des paquets manquants
    local packages_to_install=()
    for package in "${REQUIRED_PACKAGES[@]}"; do
        if ! dpkg -l "$package" 2>/dev/null | grep -q "^ii"; then
            packages_to_install+=("$package")
        else
            info "$package est d√©j√† install√©"
        fi
    done
    
    if [[ ${#packages_to_install[@]} -gt 0 ]]; then
        info "Installation des paquets : ${packages_to_install[*]}"
        if ! DEBIAN_FRONTEND=noninteractive apt install -y "${packages_to_install[@]}"; then
            error_exit "Impossible d'installer les paquets requis"
        fi
        success "Paquets install√©s avec succ√®s"
    fi
}

# Cr√©ation s√©curis√©e des r√©pertoires
create_directories() {
    local dirs=("/etc/fail2ban/filter.d" "/etc/fail2ban/jail.d" "$BACKUP_DIR" "$TEMP_DIR")
    
    for dir in "${dirs[@]}"; do
        if [[ "$SIMULATE_MODE" == "true" ]]; then
            simulate "Cr√©ation du r√©pertoire : $dir"
            continue
        fi
        
        if [[ ! -d "$dir" ]]; then
            if mkdir -p "$dir"; then
                info "R√©pertoire cr√©√© : $dir"
                chmod 755 "$dir"
            else
                error_exit "Impossible de cr√©er le r√©pertoire : $dir"
            fi
        else
            debug "R√©pertoire existant : $dir"
        fi
    done
    
    CLEANUP_NEEDED=true
